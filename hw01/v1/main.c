/*
 *https://en.wikipedia.org/wiki/UTF-8#Encoding
 *1 byte:   0xxxxxxx
 *2 bytes:  110xxxxx 10yyyyyy
 *3 bytes:  1110xxxx 10yyyyyy 10yyyyyy
 *4 bytes:  11110xxx 10yyyyyy 10yyyyyy 10yyyyyy
 */
void write_utf8(FILE *out, const int unicode) {
    // ASCII пишем как есть.
    if (unicode < ASCII_END) {
        fputc(unicode, out);
        return;
    }
    // 2 байта: 110xxxxx 10yyyyyy
    if (unicode <= 0x7FF) {
        uint8_t utf8[2];
        utf8[0] = 0xC0 | ((unicode >> 6) & 0x1F);    // страшие 5 бит + помечаем 110xxxxx
        utf8[1] = 0x80 | (unicode & 0x3F);           // младшие 6 бит + помечаем 10yyyyyy
        fwrite(utf8, 1, 2, out);
        return;
    }
    // 3 байта: 1110xxxx 10yyyyyy 10yyyyyy
    if (unicode <= 0xFFFF) {
        uint8_t utf8[3];
        utf8[0] = 0xE0 | ((unicode >> 12) & 0x0F);
        utf8[1] = 0x80 | ((unicode >> 6) & 0x3F);
        utf8[2] = 0x80 | (unicode & 0x3F);
        fwrite(utf8, 1, 3, out);
    }
    // 4 байта, эмоджи и т.д нет смысла если мы говорим об обычном тексте
}

int main(const int argc, char **argv) {
    if (argc != 4) {
        printf("USAGE: %s <file> <file encode> <output>\n", argv[0]);
        return EXIT_FAILURE;
    }

    FILE *file = fopen(argv[1], "rb");
    if (!file) {
        printf("Failed to open %s for reading!\n", argv[1]);
        return EXIT_FAILURE;
    }

    const char *encod = argv[2];

    const int is_cp1251 = strcmp(encod, "cp1251") == 0;
    const int is_koi8 = strcmp(encod, "koi8") == 0;
    const int is_iso_8859_5 = strcmp(encod, "iso-8859-5") == 0;

    if (!is_cp1251 && !is_koi8 && !is_iso_8859_5) {
        printf("Encoding not specified!\n");
        return EXIT_FAILURE;
    }

    char *out_file = argv[3];
    FILE *out = fopen(out_file, "wb");
    if (!out) {
        printf("Failed to open %s for writing!\n", out_file);
        return EXIT_FAILURE;
    }

    uint8_t buffer[1024];
    size_t bytes_read;
    while ((bytes_read = fread(buffer, 1, sizeof(buffer), file)) > 0) {
        for (size_t i = 0; i < bytes_read; ++i) {
            uint32_t unicode;
            if (is_cp1251) {
                unicode = cp1251_to_unicode(buffer[i]);
            } else if (is_koi8) {
                unicode = koi8_to_unicode(buffer[i]);
            } else {
                unicode = iso8859_5_to_unicode(buffer[i]);
            }
            write_utf8(out, (int) unicode);
        }
    }
    return EXIT_SUCCESS;
}
